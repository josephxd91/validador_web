package pe.qc.com.validator.presentacion.form.formLinux;

public class FCopiaArchivosCc {
	private String accion;
	private String ruta;
	private String archivo;
	private String destino;
	private String tk;
	private String tipoCopiado;
	
	public FCopiaArchivosCc() {
		
	}

	public String getAccion() {
		return accion;
	}

	public void setAccion(String accion) {
		this.accion = accion;
	}

	public String getRuta() {
		return ruta;
	}

	public void setRuta(String ruta) {
		this.ruta = ruta;
	}

	public String getArchivo() {
		return archivo;
	}

	public void setArchivo(String archivo) {
		this.archivo = archivo;
	}

	public String getDestino() {
		return destino;
	}

	public void setDestino(String destino) {
		this.destino = destino;
	}

	public String getTk() {
		return tk;
	}

	public void setTk(String tk) {
		this.tk = tk;
	}

	public String getTipoCopiado() {
		return tipoCopiado;
	}

	public void setTipoCopiado(String tipoCopiado) {
		this.tipoCopiado = tipoCopiado;
	}
	
}
